import { model, Schema } from 'mongoose';

const SettingSchema = new Schema({
    user: {
        type: String,
        ref: 'users',
        required: true,
    },

    status: {
        type: String,
        required: false,
    },

    presence: {
        type: Number,
        required: true,
        default: 0,
    },
});

export default model('settings', SettingSchema);

export { SettingSchema }